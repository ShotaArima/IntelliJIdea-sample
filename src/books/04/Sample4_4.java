public class Sample4_4 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;

        System.out.println("num1とnum2に色々演算を行います。");
        System.out.println("num1="+num1+"\nnum2="+num2);
        System.out.println("num1+num2="+(num1+num2));
        System.out.println("num1-num2="+(num1-num2));
        System.out.println("num1*num2="+(num1*num2));
        System.out.println("num1%num2="+(num1%num2));
        System.out.println("num1/num2="+(num1/num2));
    }
}
